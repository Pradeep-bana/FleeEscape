// Signature Pad for all canvases
document.querySelectorAll('.signature-canvas').forEach(function(canvas) {
  let ctx = canvas.getContext('2d');
  let drawing = false;
  function getPos(e) {
    let rect = canvas.getBoundingClientRect();
    if (e.touches) {
      return { x: e.touches[0].clientX - rect.left, y: e.touches[0].clientY - rect.top };
    } else {
      return { x: e.clientX - rect.left, y: e.clientY - rect.top };
    }
  }
  function start(e) {
    drawing = true;
    let pos = getPos(e);
    ctx.beginPath();
    ctx.moveTo(pos.x, pos.y);
    e.preventDefault();
  }
  function draw(e) {
    if (!drawing) return;
    let pos = getPos(e);
    ctx.lineTo(pos.x, pos.y);
    ctx.strokeStyle = "#00d4ff";
    ctx.lineWidth = 2.5;
    ctx.stroke();
    e.preventDefault();
  }
  function end() { drawing = false; }
  canvas.addEventListener('mousedown', start);
  canvas.addEventListener('mousemove', draw);
  canvas.addEventListener('mouseup', end);
  canvas.addEventListener('mouseleave', end);
  canvas.addEventListener('touchstart', start);
  canvas.addEventListener('touchmove', draw);
  canvas.addEventListener('touchend', end);
  canvas.clearSignature = function() { ctx.clearRect(0, 0, canvas.width, canvas.height); };
});
function clearSignature(btn) {
  let canvas = btn.closest('.signature-pad-box').querySelector('.signature-canvas');
  if(canvas && canvas.clearSignature) canvas.clearSignature();
}
</script>


